void main(List<String> args) {
  //Using Switch Case Statements
  String grade = 'f';
  switch (grade) {
    case 'A':
      print("Excellent");
      break;
    case 'B':
      print('Good');
      break;
    case 'C':
      print("Needs Improvments");
      break;
    default:
      print("Out of Bounce");
  }
}
